import { SimpleGrid, Card, CardHeader, Heading, CardBody, CardFooter, Button, Textarea, 
FormControl, FormLabel, Input, NumberInput, NumberInputField, NumberIncrementStepper, 
NumberDecrementStepper, NumberInputStepper, FormHelperText } from "@chakra-ui/react";
import { useState } from "react";
import { Ipost } from "../interfaces/Interfaces";



const CreatePost = () => {

    const [post, setPost] = useState<Ipost>( {
        title: "",
        startPlace: "",
        finishplace: "",
        cost: "",
        story: ""} as Ipost)

    const addPost = ():void => {
        setPost({
            title: "",
            startPlace: "",
            finishplace: "",
            cost: "",
            story: ""
        }as Ipost)
    }



    return (
        <div>
        <FormControl>
            <FormLabel colorScheme='#454545'>Trip name</FormLabel>
                <Input placeholder = 'Trip name' type='text' value={post.title} onChange={(e) => setPost(
                    {...post, title: e.target.value})}/>


            <FormLabel colorScheme='#454545'>Place of start</FormLabel>
                <Input placeholder = 'Startpoint' type='email' value={post.startPlace} onChange={(e) => setPost(
            {...post, startPlace: e.target.value})}/>


            <FormLabel colorScheme='#454545'>Place of end</FormLabel>
                <Input placeholder = 'Endpoint' type='email' value={post.finishplace} onChange={(e) => setPost(
                    {...post, finishplace: e.target.value})}/>

            <br />
            
            <FormLabel colorScheme='pink'>Cost</FormLabel>
                <Input placeholder = 'Cost' type='email' value={post.cost} onChange={(e) => setPost(
                    {...post, cost: e.target.value})}/>
            <br />
            <br />
            <FormLabel colorScheme='#454545'> Tell about your trip!</FormLabel>
                <Input placeholder = 'Write all your fun experiences!' type='text' value={post.story} onChange={(e) => setPost(
                    {...post, story: e.target.value})}/>
            <br />
            <br />
            <Button colorScheme='#454545' background= '#C9EFC7' variant='outline' onClick={addPost}>
                Post
            </Button>
        </FormControl>
    </div>
    )   
}

export default CreatePost;





